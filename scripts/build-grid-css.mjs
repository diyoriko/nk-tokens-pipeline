// Emit build/css/grid.css — a usable layout layer from the responsive tokens,
// so a developer gets the Brand-book grid as real CSS, not just raw variables.
// Produces: container max-widths, a 12/8/2-column grid utility, and breakpoint
// media queries keyed off the same numbers the Figma grid styles use.
// Run via `npm run build:grid` (and inside build:tokens).
import fs from 'node:fs';

const tok = JSON.parse(fs.readFileSync(new URL('../tokens/responsive.json', import.meta.url), 'utf8')).responsive;
const px = (t) => Number(String(t.$value).replace('px', ''));
// Tiers come from the token file itself (ordered by breakpoint), so a renamed
// or added tier can never silently vanish from the emitted CSS.
const bp = Object.keys(tok)
  .filter((k) => !k.startsWith('$'))
  .map((k) => ({
    name: k.toLowerCase(),
    min: px(tok[k]['Breakpoint-Min']),
    width: px(tok[k]['Device-Width']),
    cols: tok[k].Columns.$value,
    gutter: px(tok[k].Gutter),
    margin: px(tok[k].Margin),
  }))
  .sort((a, b) => a.min - b.min);
const base = bp.find((b) => b.min === 0);
if (!base) { console.error('✗ Grid CSS: no base tier (Breakpoint-Min = 0) in responsive.json'); process.exit(1); }

let css = `/* Generated by build-grid-css.mjs from tokens/responsive.json.
   The Brand-book grid as code: container, columns, breakpoints.
   Mirrors the Figma grid styles Grid/Mobile · Grid/Tablet · Grid/Desktop · Grid/Wide. */\n\n`;

// container: full-bleed with breakpoint side margins, capped at desktop width
css += `.nk-container{width:100%;margin-inline:auto;box-sizing:border-box;`;
css += `padding-inline:var(--nk-responsive-${base.name}-margin,${base.margin}px);}\n`;
for (const b of bp.filter((b) => b.min > 0)) {
  css += `@media (min-width:${b.min}px){.nk-container{padding-inline:var(--nk-responsive-${b.name}-margin,${b.margin}px);`;
  // centered fixed-width tiers cap the container (desktop 1440, wide 1920)
  if (b.name === 'desktop' || b.name === 'wide') css += `max-width:var(--nk-responsive-${b.name}-device-width,${b.width}px);`;
  css += `}}\n`;
}

// grid utility: column count + gutter switch per breakpoint
css += `\n.nk-grid{display:grid;gap:var(--nk-responsive-${base.name}-gutter,${base.gutter}px);`;
css += `grid-template-columns:repeat(var(--nk-responsive-${base.name}-columns,${base.cols}),1fr);}\n`;
for (const b of bp.filter((b) => b.min > 0)) {
  css += `@media (min-width:${b.min}px){.nk-grid{gap:var(--nk-responsive-${b.name}-gutter,${b.gutter}px);`;
  css += `grid-template-columns:repeat(var(--nk-responsive-${b.name}-columns,${b.cols}),1fr);}}\n`;
}

// span helpers (col-span-N), capped to the widest grid
const maxCols = Math.max(...bp.map((b) => Number(b.cols)));
css += `\n`;
for (let i = 1; i <= maxCols; i++) css += `.nk-col-${i}{grid-column:span ${i};}\n`;

// PostCSS custom-media (optional, for @media (--nk-tablet))
css += `\n@custom-media --nk-${base.name} (min-width:0);\n`;
for (const b of bp.filter((b) => b.min > 0)) css += `@custom-media --nk-${b.name} (min-width:${b.min}px);\n`;

fs.mkdirSync(new URL('../build/css/', import.meta.url), { recursive: true });
fs.writeFileSync(new URL('../build/css/grid.css', import.meta.url), css);
console.log(`✓ Grid CSS: build/css/grid.css (container + ${bp.length} breakpoints + 12-col grid).`);
